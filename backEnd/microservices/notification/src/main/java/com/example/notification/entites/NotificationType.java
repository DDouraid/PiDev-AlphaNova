package com.example.notification.entites;

public enum NotificationType {
    SYSTEM,     // Notification système (par exemple, notifications de maintenance)
    APPLICATION, // Notification liée à une application (par exemple, acceptation d'une candidature)
    DOCUMENT,    // Notification liée à un document (par exemple, un document a été mis à jour)
    REMINDER;    // Notification de rappel (par exemple, un événement à venir)
}
